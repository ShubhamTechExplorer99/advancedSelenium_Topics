package javaPackage;

public class Variable5 {
	
	public static void main(String[] args) {
		
		int x=500; //Defining a local variable
		
		//Calling x variable to printing the  values
		System.out.println(x); //500
		
		//Calling x variable for initializing y
		int y = x; //500
		System.out.println(y); //500
	}

}
