package javaPackage;

public class Variable1 {

	public static void main(String[] args) {

		int a = 5; // int type of local variables
		System.out.println(a); // 5 -callig a variables

		int x = 10; // int type of local variables
		System.out.println(x); // 10-calling a variables

		x = 20; // Modificaion of a value
		System.out.println(x); // 20

		char c = 's'; // char type local variables ,should be placed in single quote
		System.out.println(c); // s

		boolean b = true;
		System.out.println(b); // true
		b = false; // modification value
		System.out.println(b); // false

		System.out.println(3 > 12);

		int e = 100; // int type of loacal variables
		int f = 200; // int type of loacal variables

		boolean z = e > f;
		System.out.println(z); // false
	}

}
