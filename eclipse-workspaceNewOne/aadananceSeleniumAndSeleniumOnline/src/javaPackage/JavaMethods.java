package javaPackage;

public class JavaMethods {

	// 1.No parameter & No returns value

	void m1() {
		System.out.println("No parameter & No returns value methods");

	}

	// 2.No parameter & returns value

	String m2() {
		return "No parameter  & returns value";
	}

	// 3.Takes parameter & No returns value

	void m3(String name) {
		System.out.println("Takes parameter & No returns value");
	}

	// 4.Takes parameter & returns value

	String m4(String name, int id) {
		return "Takes parameter & returns value";
	}

}
